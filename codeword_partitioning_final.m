%% 2. COMPLETE GRID INTERSECTION CALCULATION ENGINE

numCodebooks = 9;

% Preallocate a full 9x9 numeric matrix for the grid values
gridMatrix = zeros(numCodebooks, numCodebooks);

% Loop through every row and every column to build a full symmetric matrix
for i = 0:(numCodebooks - 1)
    for j = 0:(numCodebooks - 1)
        
        varNameA = sprintf('codebook_%d', i);
        varNameB = sprintf('codebook_%d', j);
        
        c_A = evalin('base', varNameA);
        c_B = evalin('base', varNameB);
        
        % If either codebook is empty, leave it as 0 to avoid crashing
        if isempty(c_A) || isempty(c_B)
            gridMatrix(i+1, j+1) = 0;
            continue;
        end
        
        if i == j
            % Diagonal element: Size of the codebook itself (e.g., 259)
            gridMatrix(i+1, j+1) = size(c_A, 1);
        else
            % Off-diagonal: Perform the row intersection pattern check
            common_matrix = intersect(c_A, c_B, 'rows');
            gridMatrix(i+1, j+1) = size(common_matrix, 1);
        end
        
        % Silent individual workspace variable generation for tracking pairs (only for distinct pairs)
        if j > i
            workspaceVarName = sprintf('c%d_c%d', i, j);
            assignin('base', workspaceVarName, intersect(c_A, c_B, 'rows'));
        end
    end
end

