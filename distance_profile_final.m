%% 2. PROCESSING AND PROCESSING LOOPS
numCodebooks = 9;
% Preallocate arrays for our final summary table data
codebookNames = cell(numCodebooks, 1);
minDistances = zeros(numCodebooks, 1);
maxDistances = zeros(numCodebooks, 1);
numCodewords = zeros(numCodebooks, 1);

for idx = 0:8
    % Dynamically pull the variable from the workspace by its name
    varName = sprintf('codebook_%d', idx);
    currentCode = evalin('base', varName);
    
    tableIdx = idx + 1; % Table row indexing needs to start at 1
    codebookNames{tableIdx} = sprintf('Codebook_%d', idx);
    
    % If the codebook was left empty, skip to prevent crashing
    if isempty(currentCode)
        minDistances{tableIdx} = NaN;
        maxDistances{tableIdx} = NaN;
        numCodewords{tableIdx} = 0;
        continue;
    end
    
    numCodes = size(currentCode, 1);
    numCodewords(tableIdx) = numCodes;
    
    % Initialize Hamming distance matrix
    hammingDistances = zeros(numCodes);
    
    % Calculate Hamming distances
    for i = 1:numCodes
        for j = 1:numCodes
            hammingDistances(i, j) = sum(currentCode(i, :) ~= currentCode(j, :));
        end
    end
    
    % Store the matrix directly into the Workspace (e.g., Matrix_Codebook_0)
    % Semicolon prevents it from printing to the Command Window
    assignin('base', sprintf('Matrix_Codebook_%d', idx), hammingDistances);
    
    % Filter out self-comparison zeros to find true dmin
    filteroutpositivevalues = hammingDistances(hammingDistances > 0);
    
    if ~isempty(filteroutpositivevalues)
        minDistances(tableIdx) = min(filteroutpositivevalues);
        maxDistances(tableIdx) = max(filteroutpositivevalues);
    else
        minDistances(tableIdx) = 0; 
        maxDistances(tableIdx) = 0;
    end
end

