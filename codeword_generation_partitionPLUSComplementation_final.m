% =========================================================================
% MATLAB Script: GLOBAL CODEBOOK ALIGNMENT ENGINE (259 x 9 COMPLETE UNIFICATION)
% =========================================================================

% Ensure all individual codebook variables are accessible in your workspace:
% codebook_0, codebook_1, ..., codebook_8

global_books = {codebook_0, codebook_1, codebook_2, codebook_3, ...
                codebook_4, codebook_5, codebook_6, codebook_7, codebook_8};

n_rows = 259;
n_books = 9;

% Columns 1 to 9 correspond directly to CB_0 through CB_8
global_alignment_map = zeros(n_rows, n_books);
global_alignment_map(:, 1) = (1:n_rows)'; % Anchor on Codebook 0

ref_book = global_books{1};

fprintf('\n=======================================================\n');
fprintf('       EXECUTING GLOBAL MULTI-COSET ALIGNMENT ENGINE    \n');
fprintf('=======================================================\n');

for c = 2:n_books
    target_book = global_books{c};
    current_book_idx = c - 1; % 0-based naming for display
    
    remaining_ref_idx = (1:n_rows)';
    remaining_target_idx = (1:n_rows)';
    
    % Stage 1: Resolve Direct Overlaps (Rule 1)
    [~, idx_ref, idx_tar] = intersect(ref_book, target_book, 'rows', 'stable');
    global_alignment_map(idx_ref, c) = idx_tar;
    
    remaining_ref_idx(ismember(remaining_ref_idx, idx_ref)) = [];
    remaining_target_idx(ismember(remaining_target_idx, idx_tar)) = [];
    
    % Stage 2: Resolve Complementary Overlaps (Rule 2)
    if ~isempty(remaining_ref_idx)
        [~, idx_ref_comp, idx_tar_comp] = intersect(~ref_book(remaining_ref_idx, :), ...
                                                    target_book(remaining_target_idx, :), ...
                                                    'rows', 'stable');
        
        if ~isempty(idx_ref_comp)
            actual_ref_idx = remaining_ref_idx(idx_ref_comp);
            actual_tar_idx = remaining_target_idx(idx_tar_comp);
            
            global_alignment_map(actual_ref_idx, c) = actual_tar_idx;
            
            remaining_ref_idx(idx_ref_comp) = [];
            remaining_target_idx(idx_tar_comp) = [];
        end
    end
    
    % Stage 3: Greedy Multi-Coset Resolution Tail
    coset_count = 0;
    while ~isempty(remaining_ref_idx)
        coset_count = coset_count + 1;
        sub_ref = ref_book(remaining_ref_idx, :);
        sub_tar = target_book(remaining_target_idx, :);
        
        % Generate local cross-XOR landscape
        cross_masks = zeros(size(sub_ref,1) * size(sub_tar,1), 16);
        k_idx = 1;
        for i = 1:size(sub_ref,1)
            for j = 1:size(sub_tar,1)
                cross_masks(k_idx, :) = xor(sub_ref(i, :), sub_tar(j, :));
                k_idx = k_idx + 1;
            end
        end
        
        % Find the absolute most dominant translation vector
        mask_strings = string(zeros(size(cross_masks, 1), 1));
        for k = 1:size(cross_masks, 1)
            mask_strings(k) = sprintf('%d ', cross_masks(k, :));
        end
        [u_masks, ~, g_idx] = unique(mask_strings);
        [~, max_idx] = max(histcounts(g_idx, 1:(numel(u_masks)+1)));
        winning_mask = sscanf(u_masks(max_idx), '%d')';
        
        % Map elements using the winning translation key
        transformed_sub_ref = xor(sub_ref, winning_mask);
        [~, match_sub_ref, match_sub_tar] = intersect(transformed_sub_ref, sub_tar, 'rows', 'stable');
        
        actual_ref_idx = remaining_ref_idx(match_sub_ref);
        actual_tar_idx = remaining_target_idx(match_sub_tar);
        
        global_alignment_map(actual_ref_idx, c) = actual_tar_idx;
        
        remaining_ref_idx(match_sub_ref) = [];
        remaining_target_idx(match_sub_tar) = [];
    end
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    % --- ADD THIS AFTER THE WINNING_MASK LOOP ---

if ~isempty(remaining_target_idx)
    fprintf('   -> Mask search incomplete. Applying brute-force fallback for %d rows...\n', length(remaining_target_idx));
    
    for i = 1:length(remaining_target_idx)
        target_row = current_target_book(remaining_target_idx(i), :);
        
        % Calculate Hamming distance to every row in codebook_0
        % (This ensures we find the closest match)
        dists = sum(abs(codebook_0 - target_row), 2); 
        [min_val, match_idx] = min(dists);
        
        % Safety check: If we found a perfect match (dist 0), assign it
        if min_val == 0
            global_alignment_map(match_idx, current_book_idx) = remaining_target_idx(i);
        else
            % If no perfect match found, warn the user
            fprintf('   -> Warning: No match found for row %d\n', remaining_target_idx(i));
        end
    end
end
% ---------------------------------------------
    %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    fprintf('-> Codebook %d alignment complete. Remaining unmatched rows: 0\n', current_book_idx);
end
