% This engine maps all rows of the core cluster books directly back to 
% Codebook 0 using an automatic multi-rule tracking array.

core_indices = [1, 5, 7, 8]; % Representing CB_0, CB_4, CB_6, CB_7
ref_book = all_codebooks{1}; % CB_0 is our master anchor coordinate
n_rows = size(ref_book, 1);

% Master Map: Rows = Codeword Index (1 to 259)
% Columns: [CB_0_Row, CB_4_Matched_Row, CB_6_Matched_Row, CB_7_Matched_Row]
master_alignment_map = zeros(n_rows, 4);
master_alignment_map(:, 1) = (1:n_rows)'; % Column 1 is just the anchor index

cluster_books = {all_codebooks{1}, all_codebooks{5}, all_codebooks{7}, all_codebooks{8}};
cluster_names = [0, 4, 6, 7];

fprintf('\n=======================================================\n');
fprintf('        BUILDING UNIFIED MASTER CLUSTER INDEX MAP       \n');
fprintf('=======================================================\n');

for c = 2:4 % Aligning CB_4, CB_6, and CB_7 back to CB_0
    target_book = cluster_books{c};
    current_name = cluster_names(c);
    
    remaining_ref_idx = (1:n_rows)';
    remaining_target_idx = (1:n_rows)';
    
    % Step 1: Resolve Rule 1 (Direct Intersection Match)
    [~, idx_ref, idx_tar] = intersect(ref_book, target_book, 'rows', 'stable');
    master_alignment_map(idx_ref, c) = idx_tar;
    
    remaining_ref_idx(ismember(remaining_ref_idx, idx_ref)) = [];
    remaining_target_idx(ismember(remaining_target_idx, idx_tar)) = [];
    
    % Step 2: Resolve Rule 2 (Inversion/Complement Match)
    if ~isempty(remaining_ref_idx)
        [~, idx_ref_comp, idx_tar_comp] = intersect(~ref_book(remaining_ref_idx, :), ...
                                                    target_book(remaining_target_idx, :), ...
                                                    'rows', 'stable');
        
        actual_ref_idx = remaining_ref_idx(idx_ref_comp);
        actual_tar_idx = remaining_target_idx(idx_tar_comp);
        
        master_alignment_map(actual_ref_idx, c) = actual_tar_idx;
        
        remaining_ref_idx(idx_ref_comp) = [];
        remaining_target_idx(idx_tar_comp) = [];
    end
    
    % Step 3: Resolve Remaining Multi-Coset Tail using the Greedy Engine
    while ~isempty(remaining_ref_idx)
        sub_ref = ref_book(remaining_ref_idx, :);
        sub_tar = target_book(remaining_target_idx, :);
        
        % Calculate cross-XOR profile for current unresolved pool
        cross_masks = zeros(size(sub_ref,1) * size(sub_tar,1), 16);
        k_idx = 1;
        for i = 1:size(sub_ref,1)
            for j = 1:size(sub_tar,1)
                cross_masks(k_idx, :) = xor(sub_ref(i, :), sub_tar(j, :));
                k_idx = k_idx + 1;
            end
        end
        
        % Vectorized frequency evaluation
        mask_strings = string(zeros(size(cross_masks, 1), 1));
        for k = 1:size(cross_masks, 1)
            mask_strings(k) = sprintf('%d ', cross_masks(k, :));
        end
        [u_masks, ~, g_idx] = unique(mask_strings);
        [~, max_idx] = max(histcounts(g_idx, 1:(numel(u_masks)+1)));
        winning_mask = sscanf(u_masks(max_idx), '%d')';
        
        % Match using the winning coset vector
        transformed_sub_ref = xor(sub_ref, winning_mask);
        [~, match_sub_ref, match_sub_tar] = intersect(transformed_sub_ref, sub_tar, 'rows', 'stable');
        
        actual_ref_idx = remaining_ref_idx(match_sub_ref);
        actual_tar_idx = remaining_target_idx(match_sub_tar);
        
        master_alignment_map(actual_ref_idx, c) = actual_tar_idx;
        
        remaining_ref_idx(match_sub_ref) = [];
        remaining_target_idx(match_sub_tar) = [];
    end
    
    fprintf('-> Configuration Map for Codebook %d completed successfully.\n', current_name);
end

fprintf('-------------------------------------------------------\n');
fprintf('🎉 ALL CORE CLUSTER CODES COMPRESSED INTO A UNIFIED MAP!\n');
fprintf('Dimensions of Master Map Array: %d x %d\n', size(master_alignment_map));
fprintf('=======================================================\n');

% Print out a preview of the first 10 aligned row indices
PreviewTable = array2table(master_alignment_map(1:10, :), ...
