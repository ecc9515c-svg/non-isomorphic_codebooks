% =========================================================================
% REVERSE-ENGINEER MASK REGISTRY
% =========================================================================
% Assumes: 
% 1. 'global_alignment_map' is loaded.
% 2. 'codebook_0' to 'codebook_8' are loaded in the workspace.

mask_registry = cell(259, 9); % Stores the transformation for every row

for c = 2:9 % Columns 2 to 9 represent CB1 through CB8
    current_target_book = eval(['codebook_', num2str(c-1)]);
    
    for r = 1:259
        % Get the source row from CB0
        source_idx = global_alignment_map(r, c);
        source_row = codebook_0(source_idx, :);
        
        % Get the actual row from the target codebook
        target_row = current_target_book(r, :);
        
        % Determine Rule
        if isequal(source_row, target_row)
            mask_registry{r, c} = 'IDENTITY';
            
        elseif isequal(source_row, ~target_row) || isequal(source_row, bitxor(target_row, 1))
            % Handle both logical NOT and binary complement
            mask_registry{r, c} = 'COMPLEMENT';
            
        else
            % The mask is simply the XOR difference
            % A XOR Mask = B  =>  Mask = A XOR B
            mask_registry{r, c} = bitxor(source_row, target_row);
        end
    end
end

fprintf('Mask Registry derived successfully.\n');


