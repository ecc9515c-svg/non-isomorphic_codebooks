% =========================================================================
% SIMPLE RECONSTRUCTION SCRIPT (NO FUNCTIONS)
% =========================================================================
% This script uses the data in your workspace to reconstruct any codebook.
% Ensure codebook_0, global_alignment_map, and mask_registry exist.

% 1. Input desired codebook
cb_idx = input('Which codebook index would you like to generate? (1 to 8): ');
target_col = cb_idx + 1; % Map index: CB1 is column 2, CB8 is column 9

% 2. Initialize container
reconstructed_cb = zeros(259, 16);

% 3. Iterate through all 259 rows
for r = 1:259
    % Get the row index from your correlation map
    master_idx = global_alignment_map(r, target_col);
    
    % Get the source row from parent codebook
    source_row = codebook_0(master_idx, :);
    
    % Retrieve the rule from your registry
    rule = mask_registry{r, target_col};
    %%%%%%%%%%
    % --- ADD THIS SAFETY CHECK ---
if isempty(rule)
    % This handles the CB0 case (Master Codebook)
    reconstructed_cb(r, :) = source_row;
    continue; % Move to the next row
end
% -----------------------------
    %%%%%%%%%%%%%%
    % Apply the rule (Identity, Complement, or XOR)
    if ischar(rule) && strcmp(rule, 'IDENTITY')
        reconstructed_cb(r, :) = source_row;
        
    elseif ischar(rule) && strcmp(rule, 'COMPLEMENT')
        % Apply bitwise NOT (bitxor with 1 is a logical inversion for 16-bit)
        reconstructed_cb(r, :) = bitxor(source_row, 1);
        
    else
        % It is an XOR mask (the mask IS the rule)
        reconstructed_cb(r, :) = bitxor(source_row, rule);
    end
end

% 4. Final sorting (ascending order)
reconstructed_cb = sortrows(reconstructed_cb);

% 5. Output
